    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required|string|max:255',
        ]);

        Category::create([
            'user_id' => auth()->id(),
            'name' => $request->name,
            'color' => $request->color,
            'icon' => $request->icon,
        ]);

        return redirect()->route('categories.index');
    }

